"""
5.	จงเขียนโปรแกรมที่หาตัวเลขระหว่าง 1000 และ 3000 ที่ทุกหลักเป็นเลขคู่ (รวม 1000 และ 3000) โดยแสดงผลในรูปแบบ คั่นด้วย comma 
!! ตัวสุดตัด ,
"""

for num in range(1000, 3001, 2):
    if (num % 10) % 2 == 0 and ((num // 10) % 10) % 2 == 0 and ((num // 100) % 10) % 2 == 0 and ((num // 1000) % 10) % 2 == 0:
        if num != 2888:
            print(num, end=", ")
        else:
            print(num)
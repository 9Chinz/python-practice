### เศษคละ คือ เศษส่วนที่เป็นผลบวกของจำนวนเต็มกับเศษส่วนแท้
### เช่น 25/7 = 3 + 4/7 
#---------------------------------------------
num1 = int(input("Enter input number 1 = "))
num2 = int(input("Enter input number 2 = "))
x = num1 // num2
x2 = num1 % num2
print(f"{num1}/{num2} = {x} + {x2}/{num2}")